//(3)查找需要遍历每一个数据去判断, 时间复杂度O(n)

typedef struct TreeNode {
    int data;
    struct TreeNode* left;
    struct TreeNode* right;
}TN;

void PreOrder(TN* p) {
    if (p==NULL) return;
    printf("%d ",p->data);
    PreOrder(p->left);
    PreOrder(p->right);
}

void MidOrder(TN* p) {
    if (p==NULL) return;
    MidOrder(p->left);
    printf("%d ",p->data);
    MidOrder(p->right);
}

void PostOrder(TN* p) {
    if (p==NULL) return;
    PostOrder(p->left);
    PostOrder(p->right);
    printf("%d ",p->data);
}

